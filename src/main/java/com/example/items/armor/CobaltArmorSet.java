package com.example.items.armor;

import net.minecraft.item.ArmorItem;

public class CobaltArmorSet {

    public final ArmorItem helmet;
    public final ArmorItem chestplate;
    public final ArmorItem leggings;
    public final ArmorItem boots;

    public CobaltArmorSet(ArmorItem helmet, ArmorItem chestplate, ArmorItem leggings, ArmorItem boots) {
        this.helmet = helmet;
        this.chestplate = chestplate;
        this.leggings = leggings;
        this.boots = boots;
    }
}
